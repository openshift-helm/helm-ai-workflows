def print_results(pydantic_models: List[str], scored_structs: List[StructRelevance]):
    """Pretty print the results."""

    print("=" * 80)
    print("TOP 10 MOST RELEVANT STRUCTS")
    print("=" * 80)
    print()

    for i, struct in enumerate(scored_structs[:10], 1):
        print(f"{i}. {struct.struct_name} (score: {struct.score:.2f})")
        print(f"   📁 File: {struct.file_path}")
        print(f"   💡 Reasons: {', '.join(struct.reasons[:3])}")
        if len(struct.reasons) > 3:
            print(f"              ... and {len(struct.reasons) - 3} more")
        print()

    print()
    print("=" * 80)
    print("GENERATED PYDANTIC MODELS")
    print("=" * 80)
    print()
    print("from pydantic import BaseModel")
    print("from typing import Optional, Any")
    print()
    print()

    for model in pydantic_models:
        print(model)
        print()
        print()


# ============================================================================
# CLI Interface
# ============================================================================

def main():
    """Main CLI entry point."""

    if len(sys.argv) < 3:
        print(__doc__)
        sys.exit(1)

    ast_json_path = sys.argv[1]
    jira_input = sys.argv[2]
    output_file = sys.argv[3] if len(sys.argv) > 3 else None

    # Read JIRA text
    if jira_input == "-":
        print("📥 Reading JIRA text from stdin...", file=sys.stderr)
        jira_text = sys.stdin.read()
    else:
        print(f"📥 Reading JIRA text from {jira_input}...", file=sys.stderr)
        with open(jira_input) as f:
            jira_text = f.read()

    print(file=sys.stderr)

    # Run analysis
    try:
        models, scored = analyze_jira_and_generate_models(jira_text, ast_json_path)

        # Determine output destination
        if output_file:
            print(f"💾 Writing output to {output_file}...", file=sys.stderr)
            with open(output_file, 'w') as f:
                # Redirect stdout to file
                original_stdout = sys.stdout
                sys.stdout = f
                print_results(models, scored)
                sys.stdout = original_stdout
            print(f"✅ Done! Output saved to {output_file}", file=sys.stderr)
        else:
            print_results(models, scored)

    except FileNotFoundError as e:
        print(f"❌ Error: {e}", file=sys.stderr)
        sys.exit(1)
    except json.JSONDecodeError as e:
        print(f"❌ Error parsing AST JSON: {e}", file=sys.stderr)
        sys.exit(1)


if __name__ == "__main__":
    main()

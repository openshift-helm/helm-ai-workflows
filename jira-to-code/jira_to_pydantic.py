# Pydantic Generation
# ============================================================================

def generate_pydantic_from_struct(struct: StructRelevance) -> str:
    """
    Generate Pydantic model code from Go struct.

    Maps Go types to Python types and handles optionality based on
    JSON tags and pointer types.
    """
    lines = []
    lines.append(f"class {struct.struct_name}(BaseModel):")
    lines.append(f'    """')
    lines.append(f'    Generated from {struct.file_path}')
    if struct.doc:
        lines.append(f'    ')
        lines.append(f'    {struct.doc.strip()}')
    lines.append(f'    ')
    lines.append(f'    Relevance Score: {struct.score:.2f}')
    lines.append(f'    Reasons: {", ".join(struct.reasons[:3])}')
    lines.append(f'    """')
    lines.append("")

    if not struct.fields:
        lines.append("    pass")
        return "\n".join(lines)

    for field in struct.fields:
        # Handle embedded fields (no name)
        if not field["name"]:
            continue

        field_name = to_snake_case(field["name"])
        go_type = field["type"]

        # Check for JSON tag to determine optionality
        tag = field.get("tag", "")
        is_optional = "omitempty" in tag or go_type.startswith("*")

        # Convert type
        field_type = go_type_to_python(go_type.lstrip("*"))

        # Generate field line
        if is_optional:
            lines.append(f"    {field_name}: Optional[{field_type}] = None")
        else:
            lines.append(f"    {field_name}: {field_type}")

    return "\n".join(lines)


def go_type_to_python(go_type: str) -> str:
    """Map Go types to Python/Pydantic types."""

    # Basic types
    type_map = {
        "string": "str",
        "bool": "bool",
        "int": "int",
        "int32": "int",
        "int64": "int",
        "uint": "int",
        "uint32": "int",
        "uint64": "int",
        "float32": "float",
        "float64": "float",
        "interface{}": "Any",
        "error": "str",  # Go error type
    }

    if go_type in type_map:
        return type_map[go_type]

    # Arrays/Slices
    if go_type.startswith("[]"):
        inner_type = go_type[2:]
        inner_python = go_type_to_python(inner_type)
        return f"list[{inner_python}]"

    # Maps
    if go_type.startswith("map["):
        match = re.match(r"map\[(.*?)\](.*)", go_type)
        if match:
            key_type = go_type_to_python(match.group(1))
            val_type = go_type_to_python(match.group(2))
            return f"dict[{key_type}, {val_type}]"

    # Complex types - might be nested structs, keep as string for now
    # In a real implementation, you'd generate nested models
    return f'"{go_type}"'  # Quote complex types


def to_snake_case(name: str) -> str:
    """Convert PascalCase/camelCase to snake_case."""
    s1 = re.sub('(.)([A-Z][a-z]+)', r'\1_\2', name)
    return re.sub('([a-z0-9])([A-Z])', r'\1_\2', s1).lower()


